namespace MathUtils
{
    public class Rect
    {
        public float x;
        public float y;
        public float width;
        public float height;

        public Rect(float x, float y, float w, float h)
        {
            this.x = x;
            this.y = y;
            this.width = w;
            this.height = h;
        }
    }
}